package fr.beans;

public class AnimalBean {
	private int id;
	private String name;
	private int hungry_gauge;
	private int health_gauge;
	private String description;
	private int specie_id;
	private int enclosure_id;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getHungry_gauge() {
		return hungry_gauge;
	}
	
	public void setHungry_gauge(int hungry_gauge) {
		this.hungry_gauge = hungry_gauge;
	}
	
	public int getHealth_gauge() {
		return health_gauge;
	}
	
	public void setHealth_gauge(int health_gauge) {
		this.health_gauge = health_gauge;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
	
	public int getSpecie_id() {
		return specie_id;
	}
	
	public void setSpecie_id(int specie_id) {
		this.specie_id = specie_id;
	}
	
	public int getEnclosure_id() {
		return enclosure_id;
	}
	
	public void setEnclosure_id(int enclosure_id) {
		this.enclosure_id = enclosure_id;
	}
	
	

}
