package fr.beans;

public class EmployeeBean {
	private int id;
	private String type;
	private int health_gauge;
	private String description;
	private int enclosure_id;
	private int player_id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getHealth_gauge() {
		return health_gauge;
	}
	public void setHealth_gauge(int health_gauge) {
		this.health_gauge = health_gauge;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getEnclosure_id() {
		return enclosure_id;
	}
	public void setEnclosure_id(int enclosure_id) {
		this.enclosure_id = enclosure_id;
	}
	public int getPlayer_id() {
		return player_id;
	}
	public void setPlayer_id(int player_id) {
		this.player_id = player_id;
	}
	
	

}

