package fr.beans;

public class EnclosureBean {
	private int id;
	private int locate_x;
	private int locate_y;
	private int capacity;
	private String description;
	private int animal_quantity;
	private int cleanliness_gauge;
	private int employee_slot;
	private int employee_quantity;
	private int specie;
	
	public int getId() {
		return id;
	}
		
	public void setId(int id) {
		this.id = id;
	}
	
	public int getLocate_x() {
		return locate_x;
	}
	
	public void setLocate_x(int locate_x) {
		this.locate_x = locate_x;
	}
	
	public int getLocate_y() {
		return locate_y;
	}
	
	public void setLocate_y(int locate_y) {
		this.locate_y = locate_y;
	}
	
	public int getCapacity() {
		return capacity;
	}
	
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
	
	public int getAnimal_quantity() {
		return animal_quantity;
	}
	
	public void setAnimal_quantity(int animal_quantity) {
		this.animal_quantity = animal_quantity;
	}
	
	public int getCleanliness_gauge() {
		return cleanliness_gauge;
	}
	
	public void setCleanliness_gauge(int cleanliness_gauge) {
		this.cleanliness_gauge = cleanliness_gauge;
	}
	
	public int getEmployee_slot() {
		return employee_slot;
	}
	
	public void setEmployee_slot(int employee_slot) {
		this.employee_slot = employee_slot;
	}
	
	public int getEmployee_quantity() {
		return employee_quantity;
	}
	
	public void setEmployee_quantity(int employee_quantity) {
		this.employee_quantity = employee_quantity;
	}
	
	public int getSpecie() {
		return specie;
	}
	
	public void setSpecie(int specie) {
		this.specie = specie;
	}
	
	

}
